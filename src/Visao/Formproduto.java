    private void btnsalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsalvarActionPerformed
        // TODO add your handling code here:
        if (txtnome.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Insira o nome do produto");
            txtnome.requestFocus();
            return;
        }
        if (txtdescricao.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Você deve inserir a descrição do produto");
            txtdescricao.requestFocus();
            return;
        }

        if (txtpreco_venda.getText().length() == 0) {
            JOptionPane.showConfirmDialog(rootPane, "Você deve inserir o preço diário de venda do produto");
            txtpreco_venda.requestFocus();
            return;
        }

      

        Vproduto dts = new Vproduto();
        Fproduto func = new Fproduto();

        dts.setNome(txtnome.getText());

        

        dts.setDescricao(txtdescricao.getText());
        
       

        dts.setPreco_venda(Double.parseDouble(txtpreco_venda.getText()));

        int seleccionado = cbounidade_medida.getSelectedIndex();
        dts.setUnidade_medida((String) cbounidade_medida.getItemAt(seleccionado));

        
        if (action.equals("salvar")) {
            if (func.insertar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O Produto foi registrado com sucesso");
                mostrar("");
                inabilitar();

            }

        }
        else if (action.equals("editar")){
            dts.setIdproduto(Integer.parseInt(txtidproduto.getText()));

            if (func.editar(dts)) {
                JOptionPane.showMessageDialog(rootPane, "O produto foi editado com sucesso");
                mostrar("");
                inabilitar();
            }
        }

    }//GEN-LAST:event_btnsalvarActionPerformed

