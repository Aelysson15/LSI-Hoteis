    private void btndeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeletarActionPerformed
        // TODO add your handling code here:
        if (!txtidproduto.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Tem certeza que quer excluir o Produto?","Confirmar",2);

            if (confirmacion==0) {
                Fproduto func = new Fproduto ();
                Vproduto dts= new Vproduto();

                dts.setIdproduto(Integer.parseInt(txtidproduto.getText()));
                func.eliminar(dts);
                mostrar("");
                inabilitar();

            }

        }
    }//GEN-LAST:event_btndeletarActionPerformed

